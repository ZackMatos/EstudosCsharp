using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Operacoes_OO
{
    internal class Operações
    {
        public float Somar(float x, float y)
        {
            return x + y;
        }
        public float Subtrair(float x, float y)
        {
            return x - y;
        }
        public float Multiplicar(float x, float y)
        {
            return x * y;
        }
        public float Dividir(float x, float y)
        {
            return x / y;
        }
        public float Resto(float x, float y)
        {
            return x % y;
        }
    }
}
